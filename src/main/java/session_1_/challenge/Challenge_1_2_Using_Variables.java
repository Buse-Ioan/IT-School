package session_1_.challenge;

//2. Using Variables: Write a program that defines four different variables (an integer, a double, a character, and a string), assigns them values, and then prints them out.

public class Challenge_1_2_Using_Variables {

    public static void main(String[] args) {
        //Define and initialize variables
        int theInteger = 33;
        double theDouble = 3.33;
        char theCharacter = 'A';
        String theString = "My name is Ioan";

        // Print the variables
        System.out.println("Integer: " + theInteger);
        System.out.println("Double: " + theDouble);
        System.out.println("Character: " + theCharacter);
        System.out.println("String: " + theString);
    }
}
